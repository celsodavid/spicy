        em seus planos. E isso vinha de longa data: seguindo uma orientação de sua mãe, que dizia que imóveis eram a forma mais segura de garantir o futuro, ao longo dos anos Valter foi aplicando no setor imobiliário grande parte do que ganhava e economizava vendendo viagens. Claro que, para que isso fosse possível, teve a felicidade de fazer e ver a CVC crescer e despontar como líder de mercado. Neste mesmo período, participou de grupos de investidores na construção de empreendimentos. Nestes grupos, aprendeu mais sobre o negócio e sobre as técnicas de construção. Conforme boas oportunidades iam surgindo, ele ia comprando terrenos adequados para a construção de edifícios. Finalmente, depois de quase 40 anos no setor turístico, Valter encerrou sua carreira neste segmento. Pois um novo estava só começando. Valter resolveu então, juntar parte de seus recursos para incorporar empreendimentos nos terrenos que já possuía. Neste momento nascia a Incorporadora Patriani.  
