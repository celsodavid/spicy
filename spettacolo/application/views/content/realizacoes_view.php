     <p class="txt_real">Filho de uma família humilde, Valter Patriani nasceu em 6 de janeiro de 1958, na zona rural da cidade de Novo Horizonte, interior de São Paulo. Sua família, como tantas outras naqueles tempos difíceis, decidiu migrar para a capital paulista na esperança de dias melhores. Seu irmão mais velho foi o primeiro a chegar em São Paulo. Após conseguir um emprego na indústria automobilística do ABC, trouxe seus outros irmãos, seu pai e sua mãe. Valter chegou em São Paulo com 8 anos de idade. Não em São Paulo, exatamente, mas em São Caetano do Sul. Seu primeiro emprego foi numa banca de jornal próxima de sua casa. A banca pertencia a um senhor de idade, e Valter tomava conta dela quando o senhor não estava. Depois trabalhou em outras bancas e também na distribuidora de jornais de São Caetano. Trabalhava muito feliz. Afinal, para uma família de emigrantes, trabalhar e estar empregado eram as coisas mais importantes do mundo. Mas para a mãe de   </p> 
